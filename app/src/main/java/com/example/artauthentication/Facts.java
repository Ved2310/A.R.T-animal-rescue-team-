package com.example.artauthentication;

public class Facts {
    private int mImage;
    private String mFact;

    public Facts(int mImage, String mFact){
        this.mImage = mImage;
        this.mFact = mFact;
    }

    public String getmFact() {
        return mFact;
    }

    public int getmImage() {
        return mImage;
    }
}


